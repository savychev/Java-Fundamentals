package be.intecbrussel.les4;

public class Oefening2 {
    public static void main(String[] args) {
        int i = 100; // initialisatie 'i'
        while (i >= 50) { // het lusblock gaat uitgevord worden totdat 'i' groter dan of gelijk ann 50
            System.out.println(i);
            i--; // decrementeren 'i' bij elke iteratie
        }
    }
}
