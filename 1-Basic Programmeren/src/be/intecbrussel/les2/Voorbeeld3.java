package be.intecbrussel.les2;

public class Voorbeeld3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 0;

        c = (a + b);
        System.out.println(c);

        c += a;
        System.out.println(c);

        c -= a;
        System.out.println(c);

        c *= a;
        System.out.println(c);

        c /= a;
        System.out.println(c);

        c %= a;
        System.out.println(c);
    }
}